let number = Number(prompt("Ecrire une note entre 0 et 10"));

if(number <= 3) { alert("Nul");
 } 
 else if (number >= 4 && number <6 ) {     
     alert("Moyen");
 } 
 else if(number >=6 && number <8 ) {     
     alert("Assez bien");
 } 
 else if(number >=8 && number <=9 ) {     
     alert("Bien");
 } 
 else if(number==10) {    
     alert("Excellent");
 } else {     alert("Error") }




